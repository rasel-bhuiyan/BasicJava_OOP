
package Has_Aggrigation;

public class Depertment {
    
    private String detName;
    private String Name;

    public Depertment(String detName, String Name) {
        this.detName = detName;
        this.Name = Name;
    }

    public String getDetName() {
        return detName;
    }

    public void setDetName(String detName) {
        this.detName = detName;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }
    
    
    
}
